    private String header[]={"Mã sách", "Tên sách", "Tác giả", "Nhà xuất bản", "Năm xuất bản", "Đơn giá", "Giới thiệu"};
    private DefaultTableModel tblModel=new DefaultTableModel(header,0);
    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        ResultSet rs=null;
        try{
            if(id==-1){
                JOptionPane.showMessageDialog(this, "Bạn chưa chọn tìm kiếm theo tiêu chí !");
                return;
            }
            rs= new QLSachModify().Search(txtSearch.getText(), id);
            if(rs.isBeforeFirst()==false){
                JOptionPane.showMessageDialog(this, "Không tìm thấy sách bạn cần tìm !");
                return;
            }
            Vector data=null;
            tblModel.setRowCount(0);
            while(rs.next()){
                data=new Vector();
                data.add(rs.getString("Masach_20183960"));
                data.add(rs.getString("Tensach_20183960"));
                data.add(rs.getString("Tacgia_20183960"));
                data.add(rs.getString("NhaXB_20183960"));
                data.add(rs.getInt("NamXB_20183960"));
                data.add(rs.getFloat("Dongia_20183960"));
                data.add(rs.getString("Gioithieu_20183960"));
                tblModel.addRow(data);
            }
            tableSearch.setModel(tblModel);
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void comboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboxActionPerformed
        // TODO add your handling code here:
        String selected= combox.getSelectedItem().toString();
        if(selected.equals("Mã sách")){
            id=1;
        }
        if(selected.equals("Tên sách")){
            id=2;
        }
        if(selected.equals("Tác giả")){
            id=3;
        }
        if(selected.equals("Nhà xuất bản")){
            id=4;
        }     
        if(selected.equals("Năm xuất bản")){
            id=5;
        }   
    }//GEN-LAST:event_comboxActionPerformed

    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        // TODO add your handling code here:
        int selected=tableSearch.getSelectedRow();
        if(selected!=-1){
            txtMa.setText(tblModel.getValueAt(selected, 0).toString());
            txtTen.setText(tblModel.getValueAt(selected, 1).toString());
            txtTG.setText(tblModel.getValueAt(selected, 2).toString());
            txtNXB.setText(tblModel.getValueAt(selected, 3).toString());
            txtNamXB.setText(tblModel.getValueAt(selected, 4).toString());
            txtDG.setText(tblModel.getValueAt(selected, 5).toString());
            txtAreaGT.setText(tblModel.getValueAt(selected, 6).toString());
            txtMa.setEditable(false);
            btnEdit.setEnabled(true);
            btnAdd.setEnabled(false);
        }else{
            JOptionPane.showMessageDialog(this,"Không có sách nào được chọn !");
            return;
        }
    }//GEN-LAST:event_btnSelectActionPerformed

