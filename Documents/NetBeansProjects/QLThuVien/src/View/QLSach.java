    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if(txtMa.getText().length()==0 || txtTen.getText().length()==0 || txtDG.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Mã sách, tên sách và đơn giá không được để trống !");
        }else{
            int ret=JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm sách mới?", "Yes", JOptionPane.YES_NO_OPTION);
            if(ret!=JOptionPane.YES_OPTION){
                return;
            }
            ret=new QLSachModify().Add(txtMa.getText(), txtTen.getText(),  txtTG.getText(), txtNXB.getText(), Integer.valueOf(txtNamXB.getText().toString()), Float.valueOf(txtDG.getText().toString()), txtAreaGT.getText());
            if(ret!=-1){
                JOptionPane.showMessageDialog(this, "Bạn đã thêm mới một sách thành công !");
            }
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int ret=JOptionPane.showConfirmDialog(this, "Bạn muốn xóa sách này ?","Yes", JOptionPane.YES_NO_OPTION);
        if(ret!=JOptionPane.YES_OPTION){
            return;
        }
        int selected= tableSearch.getSelectedRow();
        if(selected!=-1){
            String data= tblModel.getValueAt(selected, 0).toString();
            ret= new QLSachModify().Delete(data);
            if(ret!=-1){
                JOptionPane.showMessageDialog(this, "Bạn đã xóa thành công !");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Lỗi không thể xóa !");
            return;
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
    
